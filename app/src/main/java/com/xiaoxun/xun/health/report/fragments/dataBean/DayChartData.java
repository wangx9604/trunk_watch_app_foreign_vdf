package com.xiaoxun.xun.health.report.fragments.dataBean;

public class DayChartData {
    private String timeStamp;
    private int value;
    public DayChartData(String time,int v){
        timeStamp = time;
        value = v;
    }

    public int getValue() {
        return value;
    }

    public String getTimeStamp() {
        return timeStamp;
    }

    @Override
    public String toString() {
        return "DayChartData{" +
                "timeStamp='" + timeStamp + '\'' +
                ", value=" + value +
                '}';
    }
}
