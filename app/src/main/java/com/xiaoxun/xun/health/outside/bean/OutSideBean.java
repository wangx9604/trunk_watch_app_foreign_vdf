package com.xiaoxun.xun.health.outside.bean;

public class OutSideBean {
    int duraValue;  //时长
    int levle;  //紫外线等级
    String timeStamp;   //统计时间

    public OutSideBean(int value,int lvl,String time){
        duraValue = value;
        levle = lvl;
        timeStamp = time;
    }

    public int getDuraValue() {
        return duraValue;
    }

    public int getLevle() {
        return levle;
    }

    public String getTimeStamp() {
        return timeStamp;
    }

    public String toFormatString(){
        return "level = " + duraValue + " | duration = " + duraValue + " | timestamp = " + timeStamp;
    }
}
