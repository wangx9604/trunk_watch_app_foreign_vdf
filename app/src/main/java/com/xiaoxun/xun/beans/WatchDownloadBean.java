public class WatchDownloadBean {
    int sn;//唯一标识
    String file;//文件名
    String data;//文件数据
    int type;//文件类型 0:故事
    int size;//文件大小
    String updateTS;//文件更新时间
    String url;//下载地址
    int status;//下载状态 100：下载完成 105:下载失败
    String md5;

    public static final int DOWNLOAD_STATUS_FAIL = 105;
    public static final int DOWNLOAD_STATUS_SUCCESS = 100;

    public static final int DOWNLOAD_TYPE_STORY = 0;

    public String getMd5() {
        return md5;
    }

    public void setMd5(String md5) {
        this.md5 = md5;
    }

    public int getSn() {
        return sn;
    }

    public void setSn(int sn) {
        this.sn = sn;
    }

    public String getFile() {
        return file;
    }

    public void setFile(String file) {
        this.file = file;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public String getUpdateTS() {
        return updateTS;
    }

    public void setUpdateTS(String updateTS) {
        this.updateTS = updateTS;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
}
