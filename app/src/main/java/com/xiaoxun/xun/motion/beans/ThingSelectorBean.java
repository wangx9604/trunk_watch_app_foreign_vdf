package com.xiaoxun.xun.motion.beans;

public class ThingSelectorBean {

    private String mThingName;
    private boolean isSelector;
    private boolean isDefault;

    public ThingSelectorBean(String mThingName, boolean isSelector, boolean isDefault) {
        this.mThingName = mThingName;
        this.isSelector = isSelector;
        this.isDefault = isDefault;
    }

    public String getmThingName() {
        return mThingName;
    }

    public void setmThingName(String mThingName) {
        this.mThingName = mThingName;
    }

    public boolean isSelector() {
        return isSelector;
    }

    public void setSelector(boolean selector) {
        isSelector = selector;
    }

    public boolean isDefault() {
        return isDefault;
    }

    public void setDefault(boolean aDefault) {
        isDefault = aDefault;
    }
}
