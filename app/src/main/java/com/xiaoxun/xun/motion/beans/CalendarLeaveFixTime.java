package com.xiaoxun.xun.motion.beans;

public class CalendarLeaveFixTime {
    private String mStartTime="06,00";
    private String mRepeatValue="1111100";
    private int mRemindValue=20;

    public String getmStartTime() {
        return mStartTime;
    }

    public void setmStartTime(String mStartTime) {
        this.mStartTime = mStartTime;
    }

    public String getmRepeatValue() {
        return mRepeatValue;
    }

    public void setmRepeatValue(String mRepeatValue) {
        this.mRepeatValue = mRepeatValue;
    }

    public int getmRemindValue() {
        return mRemindValue;
    }

    public void setmRemindValue(int mRemindValue) {
        this.mRemindValue = mRemindValue;
    }

    public void cloneTo(CalendarLeaveFixTime mFixTimeBean) {
        mFixTimeBean.setmStartTime(this.mStartTime);
        mFixTimeBean.setmRemindValue(this.mRemindValue);
        mFixTimeBean.setmRepeatValue(this.mRepeatValue);
    }
}
